# Addition
result = 5 + 4
print("5 + 3 =", result)

# Subtraction
result = 10 - 4
print("10 - 4 =", result)

# Multiplication
result = 6 * 7
print("6 * 7 =", result)

# Division
result = 20 / 4
print("20 / 4 =", result)

# Floor Division
result = 17 // 3
print("17 // 3 =", result)

# Modulus
result = 17 % 3
print("17 % 3 =", result)

# Exponentiatian
result = 2 ** 4
print("2 ** 4 =",result)

# Order of Operations
result = 2 + 4 * 4
print("2 + 4 * 4 =",result)

result = (2 + 4) * 4
print("(2 + 4) * 4 =",result)
